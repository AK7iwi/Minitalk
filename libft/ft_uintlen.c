#include "libft.h"

size_t	ft_uintlen(unsigned int nb)
{
	size_t	i;

	i = 0;
	if (nb == 0)
		return (1);
	while (nb)
	{
		i++;
		nb = nb / 10;
	}
	return (i);
}
